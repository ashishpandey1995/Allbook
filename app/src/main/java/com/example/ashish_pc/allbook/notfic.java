public class notfic {

    private String name;

    private int id;
    private int id1;
    private int id3;
    public notfic(String name,int id,int id1,int id3)
    {
        super();
        this.name=name;
        this.id=id;
        this.id1=id1;
        this.id3=id3;

    }
    public String getname()
    {
        return name;
    }

    public int getid()
    {
        return id;
    }
    public int getid1()
    {
        return id1;
    }
    public int getid3()
    {
        return id3;
    }

}
